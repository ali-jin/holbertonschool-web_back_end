#!/usr/bin/env python3
""" Basic annotations - concat """


def concat(str1: str, str2: str) -> str:
    """A function that concatenate str1 and str2

    Args:
        str1 (str): The first string
        str2 (str): The second string

    Returns:
        str: returns a concatenated string of str1 and str2
    """
    return str1 + str2
